public class Operacoes {

    public float Somar(float n1, float n2){
        float result;
        result = n1+n2;
        return result;
    }

    public float Subtrair(float n1, float n2){
        float result;
        result = n1-n2;
        return result;
    }
    public float Dividir(float n1, float n2){
        float result;
        result = n1/n2;
        return result;
    }
    public float Multiplicatir(float n1, float n2){
        float result;
        result = n1*n2;
        return result;
    }
}
